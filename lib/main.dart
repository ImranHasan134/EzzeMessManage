class MealScreen extends StatefulWidget {
  final String monthId;
  const MealScreen({super.key, required this.monthId});

  @override
  State<MealScreen> createState() => _MealScreenState();
}

class _MealScreenState extends State<MealScreen> {
  List<Member> _members = [];
  final Map<int, int> _counts = {};
  DateTime _date = DateTime.now();
  List<MealEntry> _recent = [];

  @override
  void initState() {
    super.initState();
    _load();
  }

  void _load() {
    final members = _db.getActiveMembers();
    final recent  = _db.getMealsByMonth(widget.monthId)
      ..sort((a, b) => b.date.compareTo(a.date));
    setState(() {
      _members = members;
      for (final m in members) _counts.putIfAbsent(m.id, () => 0);
      _recent = recent;
    });
  }

  void _save() {
    for (final member in _members) {
      final count = _counts[member.id] ?? 0;
      if (count > 0) {
        _db.saveMeal(MealEntry(
          memberId: member.id,
          count: count,
          date: _date,
          monthId: widget.monthId,
        ));
      }
    }
    _counts.updateAll((_, __) => 0);
    _load();
    ScaffoldMessenger.of(context)
        .showSnackBar(const SnackBar(content: Text('Meals saved!')));
  }

  @override
  Widget build(BuildContext context) {
    final total = _counts.values.fold(0, (s, v) => s + v);
    return Scaffold(
      appBar: AppBar(title: const Text('Meal entry')),
      body: ListView(padding: const EdgeInsets.all(14), children: [
        Card(
          child: Padding(
            padding: const EdgeInsets.all(16),
            child: Column(children: [
              ListTile(
                contentPadding: EdgeInsets.zero,
                leading: const Icon(Icons.calendar_today_rounded),
                title: Text(
                    '${_date.day}/${_date.month}/${_date.year}'),
                subtitle: const Text('Tap to change date'),
                onTap: () async {
                  final d = await showDatePicker(
                    context: context,
                    initialDate: _date,
                    firstDate: DateTime(2024),
                    lastDate: DateTime.now(),
                  );
                  if (d != null) setState(() => _date = d);
                },
              ),
              const Divider(),
              ..._members.map((m) => Padding(
                padding: const EdgeInsets.symmetric(vertical: 6),
                child: Row(children: [
                  CircleAvatar(
                      radius: 16,
                      child: Text(m.initials,
                          style: const TextStyle(fontSize: 11))),
                  const SizedBox(width: 10),
                  Expanded(child: Text(m.name)),
                  _Stepper(
                    value: _counts[m.id] ?? 0,
                    onChanged: (v) =>
                        setState(() => _counts[m.id] = v),
                  ),
                ]),
              )),
              const Divider(),
              Row(
                  mainAxisAlignment:
                  MainAxisAlignment.spaceBetween,
                  children: [
                    const Text('Total today'),
                    Text('$total meals',
                        style: const TextStyle(
                            fontWeight: FontWeight.bold)),
                  ]),
              const SizedBox(height: 12),
              SizedBox(
                width: double.infinity,
                child: FilledButton(
                    onPressed: total > 0 ? _save : null,
                    child: const Text('Save meals')),
              ),
            ]),
          ),
        ),
        const SizedBox(height: 16),
        Text('Recent entries',
            style: Theme.of(context)
                .textTheme
                .titleSmall
                ?.copyWith(color: Colors.grey)),
        const SizedBox(height: 8),
        ..._recent.take(15).map((e) {
          final member = _members.firstWhere(
                  (m) => m.id == e.memberId,
              orElse: () => Member(name: '?', initials: '?'));
          return Card(
            margin: const EdgeInsets.only(bottom: 6),
            child: ListTile(
              title: Text(member.name),
              subtitle: Text(
                  '${e.date.day}/${e.date.month}/${e.date.year}'),
              trailing: Row(
                  mainAxisSize: MainAxisSize.min,
                  children: [
                    Text('${e.count} meals',
                        style: const TextStyle(
                            fontWeight: FontWeight.w600)),
                    IconButton(
                        icon: const Icon(Icons.delete_outline,
                            size: 18),
                        onPressed: () {
                          _db.deleteMeal(e.id);
                          _load();
                        }),
                  ]),
            ),
          );
        }),
      ]),
    );
  }
