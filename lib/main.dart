    _db.saveCost(OtherCost(
      memberId: memberId,
      amount: amount,
      category: _category,
      note: _noteCtrl.text.trim(),
      monthId: widget.monthId,
    ));
    _amountCtrl.clear();
    _noteCtrl.clear();
    _load();
