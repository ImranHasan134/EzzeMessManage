                ]),
          ),
        )),
        Card(
          margin: const EdgeInsets.only(bottom: 6),
          child: ListTile(
            leading: const Icon(Icons.person_add_outlined),
            title: const Text('Add member'),
            onTap: () => _showMemberDialog(),
          ),
        ),

        const SizedBox(height: 12),
        // ── Appearance ───────────────────────────────────────
        _sectionLabel('Appearance'),
        Card(
          child: SwitchListTile(
            secondary: const Icon(Icons.dark_mode_outlined),
            title: const Text('Dark mode'),
            value: widget.isDark,
            onChanged: widget.onThemeToggle,
          ),
        ),

        const SizedBox(height: 12),
        // ── Data & export ─────────────────────────────────────
        _sectionLabel('Data & export'),
        Card(
          child: Column(children: [
            ListTile(
              leading: const Icon(Icons.code_rounded),
              title: const Text('Export JSON backup'),
              subtitle: const Text('Current month — importable later'),
              onTap: () => _exportSvc.shareJson(_currentMonthId()),
            ),
            const Divider(height: 0, indent: 16),
            ListTile(
              leading:
              const Icon(Icons.picture_as_pdf_rounded),
              title: const Text('Export PDF report'),
              subtitle:
              const Text('Current month — formatted summary'),
              onTap: () => _exportSvc.sharePdf(_currentMonthId()),
            ),
            const Divider(height: 0, indent: 16),
            ListTile(
              leading: const Icon(Icons.upload_file_rounded),
              title: const Text('Import from backup'),
              subtitle: const Text('Pick a .json MessManager file'),
              onTap: _runImport,
            ),
          ]),
        ),

        const SizedBox(height: 24),
        Center(
          child: Text(
            'MessManager v1.0.0 · Local-first · ObjectBox',
            style: TextStyle(
                fontSize: 11,
                color: Colors.grey.withOpacity(0.6)),
          ),
        ),
        const SizedBox(height: 8),
      ]),
    );
  }

  Widget _sectionLabel(String label) => Padding(
    padding: const EdgeInsets.only(bottom: 8, top: 4),
    child: Text(label.toUpperCase(),
        style: const TextStyle(
            fontSize: 11,
            color: Colors.grey,
            letterSpacing: 0.8,
            fontWeight: FontWeight.w500)),
  );

  void _showMemberDialog({Member? member}) {
    final nameCtrl =
    TextEditingController(text: member?.name ?? '');
    final initCtrl =
    TextEditingController(text: member?.initials ?? '');

    showDialog(
      context: context,
      builder: (_) => AlertDialog(
        title: Text(member == null ? 'Add member' : 'Edit member'),
        content: Column(mainAxisSize: MainAxisSize.min, children: [
          TextField(
            controller: nameCtrl,
            decoration:
            const InputDecoration(labelText: 'Full name'),
            autofocus: true,
            textCapitalization: TextCapitalization.words,
          ),
          const SizedBox(height: 10),
          TextField(
            controller: initCtrl,
            decoration: const InputDecoration(
                labelText: 'Initials (e.g. IM)'),
            maxLength: 2,
            textCapitalization: TextCapitalization.characters,
          ),
        ]),
        actions: [
          TextButton(
              onPressed: () => Navigator.pop(context),
              child: const Text('Cancel')),
          FilledButton(
              onPressed: () {
                final name = nameCtrl.text.trim();
                final init =
                initCtrl.text.trim().toUpperCase();
                if (name.isEmpty || init.isEmpty) return;
                final m = member ?? Member();
                m.name = name;
                m.initials = init;
                m.isActive = true;
                m.joinedMonthId = _currentMonthId();
                m.createdAt = DateTime.now();
                _db.saveMember(m);
                Navigator.pop(context);
                _loadMembers();
              },
              child: const Text('Save')),
        ],
      ),
    );
  }

  Future<void> _confirmSoftDelete(Member m) async {
    final ok = await showDialog<bool>(
      context: context,
      builder: (_) => AlertDialog(
        title: const Text('Remove member?'),
        content: Text(
            '${m.name} will be hidden but all their data is preserved.'),
        actions: [
          TextButton(
              onPressed: () => Navigator.pop(context, false),
              child: const Text('Cancel')),
          TextButton(
              style: TextButton.styleFrom(
                  foregroundColor: Colors.red),
              onPressed: () => Navigator.pop(context, true),
              child: const Text('Remove')),
        ],
      ),
    );
    if (ok == true) {
      _db.softDeleteMember(m.id);
      _loadMembers();
    }
  }

  Future<void> _runImport() async {
    final result = await _importSvc.pickFile();
    if (!mounted) return;

    if (!result.success) {
      ScaffoldMessenger.of(context).showSnackBar(SnackBar(
          content: Text(result.message),
          backgroundColor: Colors.red));
      return;
    }

    final ok = await showDialog<bool>(
      context: context,
      builder: (_) => AlertDialog(
        title: const Text('Import backup?'),
        content: Text(
            'This replaces all data for '
                '${_formatMonthId(result.backup!.monthId)}.\n\n'
                'Other months are not affected.'),
        actions: [
          TextButton(
              onPressed: () => Navigator.pop(context, false),
              child: const Text('Cancel')),
          FilledButton(
              onPressed: () => Navigator.pop(context, true),
              child: const Text('Import')),
        ],
      ),
    );

    if (ok == true && result.backup != null) {
      _importSvc.commitImport(result.backup!);
      if (mounted) {
        ScaffoldMessenger.of(context).showSnackBar(
            const SnackBar(
                content: Text('Import successful!'),
                backgroundColor: Colors.green));
        _loadMembers();
      }
    }
  }
}

